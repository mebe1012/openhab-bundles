package org.openhab.binding.nest.internal.wwn.dto;

/**
 * The data for a WWN camera activity zone.
 *
 * @author David Bennett - Initial contribution
 * @author Wouter Born - Extract ActivityZone object from Camera
 */
public class WWNActivityZone {

    private String name;
    private int id;

    public String getName() {
        return name;
    }

    public int getId() {
        return id;
    }

    @Override
    public boolean equals(Object obj) {
        if (this == obj) {
            return true;
        }
        if (obj == null) {
            return false;
        }
        if (getClass() != obj.getClass()) {
            return false;
        }
        WWNActivityZone other = (WWNActivityZone) obj;
        if (id != other.id) {
            return false;
        }
        if (name == null) {
            if (other.name != null) {
                return false;
            }
        } else if (!name.equals(other.name)) {
            return false;
        }
        return true;
    }

    @Override
    public int hashCode() {
        final int prime = 31;
        int result = 1;
        result = prime * result + id;
        result = prime * result + ((name == null) ? 0 : name.hashCode());
        return result;
    }

    @Override
    public String toString() {
        StringBuilder builder = new StringBuilder();
        builder.append("CameraActivityZone [name=").append(name).append(", id=").append(id).append("]");
        return builder.toString();
    }
}
